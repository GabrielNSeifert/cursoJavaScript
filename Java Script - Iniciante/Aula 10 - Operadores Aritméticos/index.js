// Aritméticos + - / *
//  + Adição e Concatenação.
let num1 = 10;
let num2 = 3;
console.log(num1 + num2)

//  Dois ** é potencia.
console.log(num1 ** num2)

//  % Retorna o resto da divisão.
console.log(num1 % num2)

//Sinais efetuados primeiros em uma equação:
// **
// * / %
// + -

// O operador ++ adiciona 1 na sua variavel. E o -- subtrai.
let contador = 1;
contador++; // 2
contador++; // 3
contador--; // 2
console.log(++contador); // 3

// Soma de 5 em 5:
const passo = 5;
let contador1 = 0;
contador1 += passo;// 5
contador1 += passo;// 10
contador1 += passo;// 15
console.log(contador1);
// Outra forma:
const passo2 = 5;
let contador2 = 0;
contador2 = contador2 + passo2;// 5
contador2 = contador2 + passo2;// 10
contador2 = contador2 + passo2;// 15
console.log(contador2)